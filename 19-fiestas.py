mes = input("Introduce en minusculas las tres primeras letras de un mes: ")
dia = int(input("Introduce el dia del mes: "))
if (mes == "ene") & (dia == 1):
   print("Anno nuevo")
elif (mes == "ene") & (dia == 6):
   print("Reyes")
elif (mes == "abr") & (dia == 9):
   print("Jueves santo")
elif (mes == "abr") & (dia == 10):
   print("Viernes santo")
